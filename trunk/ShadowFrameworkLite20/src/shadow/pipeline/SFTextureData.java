package shadow.pipeline;

public class SFTextureData {

	private int width;
	private int height;
	
	public SFTextureData(int width, int height) {
		super();
		this.width=width;
		this.height=height;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
	
}
